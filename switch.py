a=int(input("Enter the first number:-"))
b=int(input("Enter the second number:-"))
choice=int(input("Enter the choice:-"))
if choice==1:
    print("Addition:-",a+b)
elif choice==2:
    print("Subtraction:-",a-b)
elif choice==3:
    print("Multiplication:-",a*b)
elif choice==4:
    print("Division:-",a/b)
else:
    print("Division:-",a//b)



